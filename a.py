x = int(input("Zadej číslo x: ")) #Tady zadá uživatel číslo x
y = int(input("Zadej číslo y: ")) #Tady zadá uživatel číslo y
op = input("Zadej operátor: ")

if op == "+":
    z = x + y
elif op == "-":
    z = x - y
elif op == "*":
    z = x * y
elif op == "/":
    z = x / y

print(z) #vypíše co

